public class MatematikaCanggih {
    void pertambahan(int a, int b, int c){
        System.out.println("jumlah pertambahan dari " + a + "+" + b + "+" + c + "="+(a+b+c));
    }
    void perkalian(int a, int b, int c){
        System.out.println("jumlah perkalian dari " + a + "" + b + "" + c + "=" +(a*b*c));
    }
    void modulus(int a, int b, int c){
        System.out.println("jumlah modulus dari " + a + "%" + b + "%" + c + "=" +(a%b%c));
    
    }
}